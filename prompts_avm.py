"""
提示词模板模块

管理所有AEB场景分析相关的提示词模板
"""

from typing import Dict, Any, List

class PromptTemplate:
    """AVP场景分析提示词模板类"""
    ANALYSIS_TEMPLATE = """
    您是一位专业的汽车泊车障碍物检测分析师。请基于提供的鸟瞰图，根据图像中以特定颜色标注的感知系统输出，判断是否是因为误检导致当前倒车紧急刹车。
    
    #### 🎨 图像标注信息定义（分析依据）
    请严格依据以下颜色和图形进行判断：
    1.  **红色检测点 (RGB: 255, 0, 0)**: 感知系统输出的障碍物检测结果。形态为点状或短线（≤2×2像素）。您的**分析对象**。**注意：标注点不会出现在原始图像纹理中**。
    2.  **绿色投影区域 (RGB: 0, 255, 0)**: **邻近车辆的投影区域**。这是将周围车辆检测框投影到地面形成的多边形区域，代表相邻车辆可能占据的地面空间。
    3.  **黑色边缘线 (RGB: 0, 0, 0)**: **墙体或垂直结构与地面的交界线**。这条线用于明确墙体、柱子等垂直结构的基础部分。通常，**位于此线的墙体侧或此线边缘极近区域（地面侧）的检测点，视为对该垂直结构的检测**。
    4.  **图像中心的黑色矩形**：真实的自车（矩形上方为车头，下方为车尾），正在向后导入车位（倒车入库）。
    5.  **白色箭头**：位于自车车位中心点处，指示自车预计的倒车方向。若无此箭头，默认自车沿车位中轴线直线倒车。
    6.  **白色矩形框 (RGB: 255, 255, 255)**：**仅用于遮挡车牌信息**，与障碍物分析无关。分析时请**完全忽略**其存在，**切勿将其视为地面标线或障碍物**。
    
    #### 📊 检测点信息
    检测点以像素坐标形式提供，图像大小是640*800，图像左上角为原点(0,0)，**请务必根据给定的像素坐标，在脑海中精确定位检测点在图像中的实际位置，并结合图像内容进行判断。**。
    **重要：像素坐标是唯一可信的定位依据，您不能根据检测点相对于绿色框或其它标注的“大概位置”进行判断，必须基于坐标进行精确分析**。
    - 检测点在图像上的像素坐标：{avm}
    
    #### 🎯 核心分析任务
    1.  **轨迹相关性过滤（严格）**：筛选出位于自车**必经碰撞轨迹**上的检测点，剔除所有旁路干扰。
    2.  **功能性真伪判定（关键）**：判断位于轨迹上的检测点是**真实致命障碍物**还是**系统误判（误检）**。
    *   *特别注意：检测到轮挡/限位器，被视为误检。*
    
    #### 🔍 分析规则
    #### ✅ 场景前提
    - **自车处于向后泊车过程**，正在驶入停车位
    - **发生了紧急刹车**，需要分析感知系统检测点是否是导致刹车的原因
    
    #### 🎯 第一步：关联性筛选（是否与刹车相关）
    **判断标准**：检测点是否位于自车当前运动方向及邻近区域内可能引起碰撞的区域
    **倒车入库场景**（主要分析方向）
    **✅ 高度相关（必须分析）**：
    *   检测点严格位于**车尾正后方**或**后轮倒车轨迹线**内部。
    *   检测点位于**自车后保险杠**或**侧裙**的直接接触面上。
    *   检测点位于**自车即将泊入的车位地面范围内**。
    **❌ 无关/忽略（直接丢弃，不进入下一阶段）**：
    *   **侧向干扰**：位于自车轨迹**外侧**的点（即使距离很近，只要不阻挡倒车路径）。例如：邻近车位的限位器、相邻车辆、柱子侧面（非碰撞面）。
    *   **远端背景**：距离自车明显超过泊车停止位置的点（如远处的墙根、通道对面的物体）

    #### 🎯 第二步：真伪判断（仅针对相关检测点）
    **原则**：对每个**“相关”检测点**进行**独立判断**，**禁止聚类推断（Anti-Clustering）**
    **核心：基于像素坐标与图像内容的精确位置关系进行综合判断。分析顺序：先检查有无明确实体结构支撑，再判断是否为地面干扰**。
    **第一步：检查是否在明确实体结构上（判定为正确检测，非误检）**
    如果检测点位于以下任一**明确实体障碍物**的**关键结构部位**上，判定为**正确检测（is_misdetected: False）**：
    1.  **邻近车辆相关**：
    - **绿色框内部**：检测点像素坐标位于任意绿色多边形投影区域内。
    - **车辆实体部位**：在无绿色框标注但图像中清晰可见邻近车辆的车身、轮胎、保险杠等部位，且检测点位于这些可见实体上。
    2.  **墙体/垂直结构相关**：
    - **墙体本体（含装饰结构）**：检测点像素坐标位于黑色边缘线的墙体侧的可视墙面上。**特别注意：墙面上的白色装饰条、腰线、不同颜色的涂料分界处，均属于墙体实体表面。只要在黑线的墙体侧，即便看起来像标线，也判定为真实障碍物（is_misdetected: False）。**
    - **墙体邻近地面**：检测点像素坐标位于**地面**上，但其靠近黑色边缘线。这代表对“紧贴墙根地面”的检测，通常是感知系统对墙体障碍物的合理判断区域。
    - **可见垂直结构**：在图像中清晰可见的柱子、墙角、消防栓、收费桩等垂直障碍物的基部或主体上（即使没有黑色标注线）。
    3.  **其他真实障碍物**：
    - **低矮路缘/台阶**：地面可见的高度变化边缘。
    - **明确的行人、自行车、购物车等动态或静态物体**。
    - **车位内的实际障碍物**：如地锁（**非轮挡**）、矮桩等。
    
    **第二步：检查是否为常见误检模式（判定为误检）**
    如果检测点**不满足上述任何“明确实体结构”条件**，且符合以下特征之一，判定为**误检（is_misdetected: True）**：
    1.  **地面纹理干扰**：位于光滑、空旷的地面，地面的颜色变化、污渍、划痕、拼接缝、反光区域。
    2.  **光影干扰**：位于阴影边缘、光照变化区域、水渍反光、车灯眩光处。
    3. **特定误检目标**：位于停车限位器（轮挡）、减速带上或其附近。

    #### 🧩 分析流程
    ##### 第一步：图像分析
    **请将提供的坐标精确对应到图像中的像素点位置，并仔细审视该点所处的图像内容**：
    - **精确定位**检测点在图像上的像素坐标
    - **识别黑色矩形（自车）的位置和方向**
    - **识别白色箭头的位置和指向，若无箭头，则推断自车将沿车位中轴线直线倒车**
    - **识别所有绿色检测框的位置和范围**
    - **特别注意阴暗或光线复杂的图像环境**，根据黑色边缘线清晰地区分墙体区域（障碍区）和地面区域（行驶区）
    
    ##### 第二步：关联性筛选
    对每个检测点，判断是否与本次紧急刹车相关：
    - **相关**：检测点位于自车倒车轨迹、后部接触区或目标车位内。**进入下一步真伪判断**。
    - **不相关**：检测点位于侧方、前方（对于倒车）或远处背景。**直接忽略，不输出**。
    
    ##### 第三步：真伪判断
    对每个**“相关”检测点**进行**独立判断**，**禁止聚类推断（Anti-Clustering）**：
    1   **是否有明确实体支撑**：检查与绿色框/黑线的关系，并观察图像中该坐标点是否落在任何可见的、真实的物体上。如果是 → is_misdetected: False。
    2.  **是否为典型误检模式**：如果否，检查该点是否位于地面纹理、光影、轮挡等典型干扰物上。如果是 → is_misdetected: True。
    
    ##### 第四步：结果汇总
    整理所有相关检测点的分析结果
    
    #### 📤 输出格式
    **只输出确认误检（is_misdetected: True）的检测点**，请严格按照以下JSON格式输出：
    {{
        "positions": [
            // 如果输入坐标为空或无误检，此处为空列表 []。
            // 如果有误检，格式为： {{
            //     "pixel_coordinates": [x, y],
            // }}
        ]
    }}
    """

    @classmethod
    def format_prompt(cls, index) -> str:
        """
        格式化提示词模板（7帧版本）

        Args:
            ego_info_texts: 7帧自车信息文本列表
            critical_info_texts: 7帧关键目标信息文本列表
            template_type: 模板类型 ("full" 或 "simple")

        Returns:
            格式化后的提示词
        """

        return cls.ANALYSIS_TEMPLATE.format(**index)


class PromptValidator:
    """提示词验证器"""
    
    @staticmethod
    def validate_ego_info_format(ego_info_texts: List[str]) -> bool:
        """验证自车信息格式"""
        required_fields = ["velocity_abs", "position_xy", "heading"]
        for text in ego_info_texts:
            if not all(field in text for field in required_fields):
                return False
        return True
    
    @staticmethod
    def validate_critical_info_format(critical_info_texts: List[str]) -> bool:
        """验证关键目标信息格式"""
        required_fields = ["type", "velocity_abs", "position_xy"]
        for text in critical_info_texts:
            if "无结构化数据" in text:
                continue
            if not all(field in text for field in required_fields):
                return False
        return True
